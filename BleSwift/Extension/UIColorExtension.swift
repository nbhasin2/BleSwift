import UIKit

extension UIColor {
    
    // www.colorcombos.com/color-schemes/285/ColorCombo285.html
    
    class func chillBlue() -> UIColor {
        return self.init(red: 18/255, green: 151/255, blue: 147/255, alpha: 1.0)
    }
    
    class func emperorGray() -> UIColor {
        return self.init(red: 80/255, green: 80/255, blue: 80/255, alpha: 1.0)
    }
    
    class func eggWhite() -> UIColor {
        return self.init(red: 255/255, green: 245/255, blue: 195/255, alpha: 1.0)
    }
    
    class func aquaBlue() -> UIColor {
        return self.init(red: 155/255, green: 215/255, blue: 213/255, alpha: 1.0)
    }
    
    class func bittersweetRed() -> UIColor {
        return self.init(red: 255/255, green: 114/255, blue: 96/255, alpha: 1.0)
    }
}